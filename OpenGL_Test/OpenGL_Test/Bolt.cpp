////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////

BOLT::BOLT(float _x, float _y)
{
	x = _x;
	y = _y;
	Highlighted = false;

}

BOLT::~BOLT()
{

}

////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////

float BOLT::GetDrawX()
{
	return x;//Screen.width;
}

////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////

float BOLT::GetDrawY()
{
	return y;//Screen.height;
}

////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////

void BOLT::GetDrawColor( Color* color )
{
	color->Reset();

	if( Highlighted )
	{
		color->red = 0.8f;
		color->green = 0.8f;
		color->blue = 0.2f;
	}
	else
	{
		color->red = 0.6f;
		color->green = 0.6f;
		color->blue = 0.6f;
	}
}

////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////

bool BOLT::CanBeDeleted()
{
	return true;
}

////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////

void Anchor::GetDrawColor( Color* color )
{
	color->Reset();

	if( Highlighted )
	{
		color->red = 1.0f;
		color->green = 1.0f;
		color->blue = 0.5f;
	}
	else
	{
		color->red = 0.85f;
		color->green = 0.85f;
		color->blue = 0.75f;
	}
}

////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////

bool Anchor::CanBeDeleted()
{
	return false;
}